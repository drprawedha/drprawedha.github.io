var ab = 3
var bc = 4

function luassegitiga() {
    console.log("nilai ab adalah",ab);
    console.log("nilai bc adalah",bc);
    abp2 = Math.pow(ab,2)
    // console.log(abp2);
    bcp2 = Math.pow(bc,2)
    // console.log(bcp2);
    acp2 = (abp2+bcp2)
    // console.log(acp2);
    ac = Math.sqrt(acp2)
    console.log("panjang sisi ac adalah",ac);
}

luassegitiga()